using System.Text.RegularExpressions;

namespace MyTelegram.Domain.Specifications;

public class IsEmailAddressSpecification : Specification<string>
{
    private const string EmailPattern = @"^[A-Za-z0-9\u4e00-\u9fa5]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+$";
    //private const string EmailPattern =
    //    @"\A(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?)\Z";
    //private const string EmailPattern =
    //    @"^((([!#$%&'*+\-/=?^_`{|}~\w])|([!#$%&'*+\-/=?^_`{|}~\w][!#$%&'*+\-/=?^_`{|}~\.\w]{0,}[!#$%&'*+\-/=?^_`{|}~\w]))[@]\w+([-.]\w+)*\.\w+([-.]\w+)*)$";

    //private const string EmailPattern =
    //    @"/^[a-zA-Z0-9.!#$%&'*+\/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/";

    protected override IEnumerable<string> IsNotSatisfiedBecause(string obj)
    {
        if (!string.IsNullOrEmpty(obj))
        {
            if (!Regex.IsMatch(obj, EmailPattern))
            {
                yield return "EMAIL_INVALID";
            }
        }
    }
}
